public class Messy {
    public static void main (String [] args) {
        int first = 4;
        int second = first * 2;
        int third = first + second;
        System.out.println("Values are: " + first + ", " + second + ", " + third);
        System.out.println("first + second = "+ third);
        System.out.println("third = " + third);
    }
}
